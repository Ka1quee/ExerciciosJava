package lista110exercicios;

/*
51) Faça um programa que exiba os números de 1 a 10 usando o loop "do-while".
*/

public class Exercicio51 {
    public static void main(String[] args) {
        int i = 1;

        do {
            System.out.println(i);
            i++;
        } while (i <= 10);
    }
}
